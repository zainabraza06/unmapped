/// Look up a country by ISO2 code. Returns null if not found.
Country? countryByCode(String code) {
  try {
    return kAllCountries.firstWhere(
      (c) => c.code.toUpperCase() == code.toUpperCase(),
    );
  } catch (_) {
    return null;
  }
}
